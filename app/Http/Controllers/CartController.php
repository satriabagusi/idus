    {
        
       Cart::create([
            'user_id' => $request['user_id'],
            'product_id' => $request['product_id'],
            'quantity' => $request['quantity'],
            'total_harga' => $request['total_harga']* $request['quantity'],
        ]);
    
        return Redirect::back();
        
    }

